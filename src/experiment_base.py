from abc import ABC, abstractmethod

class BaseExperiment(ABC):
    """Abstract base class establishing the contract for all risk stratification modules."""

    def __init__(self, name):
        """Initializes the base experiment metrics and identifiers.

        Args:
            name (str): The unique descriptive name of the study.
        """
        self.name = name
        self.latex_table = ""
        self.plotly_figure = None

    @abstractmethod
    def run(self, data):
        """Executes the specific statistical or machine learning pipeline.

        Args:
            data (pd.DataFrame): The preprocessed clinical dataframe.
        """
        pass

    def export_latex(self):
        """Returns the formatted LaTeX code generated by the experiment.

        Returns:
            str: Booktabs styled LaTeX string.
        """
        return self.latex_table

    def save_interactive_plot(self, output_path):
        """Saves the generated Plotly visualization as an independent interactive HTML file.

        Args:
            output_path (str): Target destination on the file system.
        """
        if self.plotly_figure:
            self.plotly_figure.write_html(output_path)
